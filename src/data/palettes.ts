import type { Palette } from "../types";

let counter = 0;
const id = () => `seed-${Date.now().toString(36)}-${(++counter).toString(36)}`;

const createdAt = new Date().toISOString();

function p(
  name: string,
  description: string,
  mood: string,
  tags: string[],
  colors: { role: any; label: string; hex: string }[],
): Palette {
  return {
    id: id(),
    name,
    description,
    mood,
    tags,
    createdAt,
    updatedAt: createdAt,
    colors: colors.map((c) => ({
      id: `${name}-${c.role}`,
      role: c.role,
      label: c.label,
      hex: c.hex,
      locked: false,
    })),
  };
}

