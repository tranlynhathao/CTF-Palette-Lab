/**
 * Tiny Illustrator JSX automation. Opens a sibling SVG and saves a `.ai` next
 * to it. Designed to be safe: it neither overwrites without prompting nor
 * touches anything outside its own folder.
 */
const JSX_SCRIPT = `// open-and-save-as-ai.jsx
// Run from Illustrator: File → Scripts → Other Script…
// Opens logo-current-palette.svg sitting next to this script and saves a
// sibling logo-current-palette.ai. Safe: prompts before overwriting.

(function () {
  try {
    var here = File($.fileName).parent;
    var svgFile = new File(here.fsName + "/logo-current-palette.svg");
    if (!svgFile.exists) {
      alert("logo-current-palette.svg was not found next to this script.");
      return;
    }
    var doc = app.open(svgFile);
    var aiFile = new File(here.fsName + "/logo-current-palette.ai");
    if (aiFile.exists) {
      var ok = confirm("logo-current-palette.ai already exists. Overwrite?");
      if (!ok) return;
    }
    var saveOpts = new IllustratorSaveOptions();
    saveOpts.compatibility = Compatibility.ILLUSTRATOR17;
    doc.saveAs(aiFile, saveOpts);
    alert("Saved: " + aiFile.fsName);
  } catch (e) {
    alert("Error: " + e);
  }
})();
`;
